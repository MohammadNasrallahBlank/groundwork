"""Turnkey pilot: the 5 ready tasks, both conditions, 1 run each (10 sessions).

    uv run python eval/pilot.py setup    # workdirs + prompt files + run scripts
    uv run python eval/pilot.py status   # what's graded, what's pending

`setup` writes two PowerShell scripts (run_without.ps1, run_with.ps1) that run
all 5 tasks headlessly, and PILOT.md with plain-language steps.
"""
import argparse
import json
import shutil
import sys
from pathlib import Path

HERE = Path(__file__).parent
REPO = HERE.parent
RUNS = HERE / "runs"
WORK = HERE / "work"
PROMPTS = HERE / "prompts"
CONDITIONS = ("without", "with")
MODEL = "claude-sonnet-5"          # judged/run model — same in both arms


def ready_tasks() -> list[dict]:
    reg = json.loads((HERE / "tasks" / "registry.json").read_text(encoding="utf-8"))
    return [t for t in reg["tasks"] if t.get("fixture_ready")]


def _wd(task, cond) -> Path:
    return WORK / f"{task}-{cond}-1"


def _graded(task, cond) -> Path:
    return RUNS / f"{task}__{cond}__r1.json"


def setup(force: bool) -> int:
    WORK.mkdir(parents=True, exist_ok=True)
    PROMPTS.mkdir(parents=True, exist_ok=True)
    tasks = ready_tasks()
    for t in tasks:
        (PROMPTS / f"{t['id']}.txt").write_text(t["prompt"], encoding="utf-8",
                                                newline="\n")
        for cond in CONDITIONS:
            wd = _wd(t["id"], cond)
            if wd.exists() and not force and not _graded(t["id"], cond).exists():
                print(f"  keep  {wd.name}")
                continue
            if wd.exists():
                shutil.rmtree(wd)
            shutil.copytree(HERE / "tasks" / t["fixture"] / "fixture", wd)
            print(f"  made  {wd.name}")
    for cond in CONDITIONS:
        _write_phase_script(tasks, cond)
    _write_readme(tasks)
    print(f"\nWrote run_without.ps1, run_with.ps1, and PILOT.md under {HERE}")
    print(f"{len(tasks)} tasks x 2 conditions = {len(tasks) * 2} sessions.")
    return 0


def _write_phase_script(tasks: list[dict], cond: str) -> None:
    ids = ", ".join(f'"{t["id"]}"' for t in tasks)
    body = f"""# Auto-generated by pilot.py. Runs the {len(tasks)} pilot tasks
# headlessly, {cond.upper()} Groundwork. Just run this whole file.
$ErrorActionPreference = "Continue"
$repo  = "{REPO}"
$model = "{MODEL}"
$cond  = "{cond}"
$tasks = @({ids})
foreach ($t in $tasks) {{
    $wd = Join-Path $repo "eval\\work\\$t-$cond-1"
    $pf = Join-Path $repo "eval\\prompts\\$t.txt"
    Write-Host "===== RUNNING $t ($cond) ====="
    Set-Location $wd
    Get-Content -Raw $pf | claude -p --dangerously-skip-permissions --model $model
    Write-Host ""
}}
Set-Location $repo
Write-Host "ALL {len(tasks)} $cond SESSIONS DONE. Tell Claude the $cond phase finished."
"""
    (HERE / f"run_{cond}.ps1").write_text(body, encoding="utf-8", newline="\r\n")


def _write_readme(tasks: list[dict]) -> None:
    names = ", ".join(t["id"] for t in tasks)
    md = f"""# Pilot — dead-simple steps

You run two PowerShell scripts (each runs {len(tasks)} coding sessions on its
own, ~10-20 min). Claude does the grading. Tasks: {names}.

## Phase 1 - WITHOUT Groundwork
1. Open **Windows PowerShell**.
2. Paste this line and press Enter, then wait until it prints "ALL ... DONE":
   `& "{HERE / 'run_without.ps1'}"`
3. Tell Claude: "without phase done".

## Phase 2 - install Groundwork, then WITH
4. In PowerShell, run these three lines one at a time:
   `uv tool install --force "{REPO}"`
   `claude plugin marketplace add "{REPO}"`
   `claude plugin install groundwork@groundwork`
5. Paste and run, wait for "ALL ... DONE":
   `& "{HERE / 'run_with.ps1'}"`
6. Tell Claude: "with phase done".

Claude grades everything and writes eval/REPORT.md. Model: {MODEL}.
"""
    (HERE / "PILOT.md").write_text(md, encoding="utf-8", newline="\n")


def status() -> int:
    tasks = ready_tasks()
    print(f"{'task':<14}{'without':<26}{'with':<26}")
    for t in tasks:
        cells = []
        for cond in CONDITIONS:
            g = _graded(t["id"], cond)
            if g.is_file():
                rec = json.loads(g.read_text(encoding="utf-8"))
                c = rec["check"]
                cells.append(f"{'PASS' if c['pass'] else 'FAIL'} acc={c['accuracy']} "
                             f"{rec['cost']['total_tokens'] // 1000}k tok")
            else:
                cells.append("pending")
        print(f"{t['id']:<14}{cells[0]:<26}{cells[1]:<26}")
    done = sum(_graded(t["id"], c).is_file() for t in tasks for c in CONDITIONS)
    print(f"\n{done}/{len(tasks) * 2} sessions graded.")
    return 0


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("cmd", choices=["setup", "status"])
    ap.add_argument("--force", action="store_true")
    ns = ap.parse_args(argv)
    return setup(ns.force) if ns.cmd == "setup" else status()


if __name__ == "__main__":
    sys.exit(main())
